#include <stdio.h>

void	ft_swap(int	*a, int	*b);

int	main(void)
{
	int a;
	int	b;

	a = -2147483648;
	b = 2147483647;
	printf("%d\n%d\n", a, b);
	ft_swap(&a, &b);
	printf("%d\n%d\n", a, b);
	return (0);
}
