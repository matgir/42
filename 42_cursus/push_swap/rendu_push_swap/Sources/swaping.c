#include "libpushswap.h"

int	swaping(t_ps_list *stack_a)
{
	t_ps_list	*stack_b;

	stack_b = NULL;
	while (1)
	{
		if (stack_b == NULL && is_it_good(stack_a) == 0)
			return (0);
		reverse_a(&stack_a);
		push_b(&stack_a, &stack_b);
		reverse_a(&stack_a);
		push_a(&stack_a, &stack_b);
		rotate_a(&stack_a);
		rotate_a(&stack_a);
	}
	return (1);
}
