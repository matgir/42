void	ft_1_23l(int	**tab)
{
	int	h;

	h = 1;
	while (h < 5)
	{
		if (tab[h][0] == 1)
			tab[h][1] = 4;
		if (tab[h][5] == 1)
			tab[h][4] = 4;
		if (tab[h][0] == 2 && tab[h][5] == 3)
			tab[h][2] = 4;
		if (tab[h][0] == 3 && tab[h][5] == 2)
			tab[h][3] = 4;
		h++;
	}
}

void	ft_1_23c(int	**tab)
{
	int	l;

	l = 1;
	while (l < 5)
	{
		if (tab[0][l] == 1)
			tab[1][l] = 4;
		if (tab[5][l] == 1)
			tab[4][l] = 4;
		if (tab[0][l] == 2 && tab[5][l] == 3)
			tab[2][l] = 4;
		if (tab[0][l] == 3 && tab[5][l] == 2)
			tab[3][l] = 4;
		l++;
	}
}
