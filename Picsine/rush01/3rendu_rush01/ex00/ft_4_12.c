/////////////////////////////////////
// 		  remplissage ligne       //
///////////////////////////////////

void	ft_41_rpligne(int **tab)
{
	int	h;

	h = 1;
	while (h < 5)
	{
		if (tab[h][0] == 4)
		{
			tab[h][1] = 1;
			tab[h][2] = 2;
			tab[h][3] = 3;
			tab[h][4] = 4;
		}
		if (tab[h][5] == 4)
		{
			tab[h][4] = 1;
			tab[h][3] = 2;
			tab[h][2] = 3;
			tab[h][1] = 4;
		}
		h++;
	}
}

////////////////////////////////////
//       remplissage colone      //
//////////////////////////////////
void	ft_41_rpcolone(int **tab)
{
	int	l;

	l = 1;
	while (l < 5)
	{
		if (tab[0][l] == 4)
		{
			tab[1][l] = 1;
			tab[2][l] = 2;
			tab[3][l] = 3;
			tab[4][l] = 4;
		}
		if (tab[5][l] == 4)
		{
			tab[4][l] = 1;
			tab[3][l] = 2;
			tab[2][l] = 3;
			tab[1][l] = 4;
		}
		l++;
	}
}

