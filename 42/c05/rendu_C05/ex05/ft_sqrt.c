int	ft_sqrt(int nb)
{
	long int	j;

	if (nb < 0)
		return (0);
	if (nb % 10 == 2 || nb % 10 == 3 || nb % 10 == 7 || nb % 10 == 8)
		return (0);
	j = 1;
	while (j * j < nb)
		j++;
	if (j * j == nb)
		return (j);
	return (0);
}
