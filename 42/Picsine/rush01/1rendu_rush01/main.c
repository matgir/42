#include <stdlib.h>
void init_sky(int **tab);
void print_sky(int **tab);

int main(void )
{
	int tab[6][6];

///////////////////// REMPLISSAGE /////////////////////	
	int h;
	int l;

	h = 0;
	while(h < 6)
	{	
		l = 0;
		while(l <= 6)
		{
			tab[h][l] = 0;
			l++;
		}
		h++;
	}
	
	tab[0][1] = 1;
	tab[0][2] = 2;
	tab[0][3] = 3;
	tab[0][4] = 4;
	
	tab[5][1] = 4;
	tab[5][2] = 3;
	tab[5][3] = 2;
	tab[5][4] = 1;

	tab[1][0] = 1;
	tab[2][0] = 2;
	tab[3][0] = 3;
	tab[4][0] = 4;

	tab[1][5] = 4;
	tab[2][5] = 3;
	tab[3][5] = 2;
	tab[4][5] = 1;

/////////////////////////////////////
// 		  remplissage ligne       //
///////////////////////////////////
	h = 1;
	while(h < 5)
	{	
		if(tab[h][0] == 4)
		{
			l = 1;
			while(l < 5)
			{
				tab[h][l] = l;
				l++;
			}	
		}
		h++;
	}



////////////////////////////////////
//  remplissage inverse ligne    //
//////////////////////////////////
	
	int i;
	
	h = 1;
	while(h < 5)
	{	
		if(tab[h][5] == 4)
		{	
			i = 1;
			l = 4;
			while(l > 0)
			{
				tab[h][l] = i;
				l--;
				i++;
			}	
		}
		h++;
	}

////////////////////////////////////
//       remplissage colone      //
//////////////////////////////////
	

	l = 1;
	while(l < 5)
	{	
		if(tab[0][l] == 4)
		{
			h = 1;	
			while(h < 5)
			{
				tab[h][l] = h;
				h++;
			}	
		}
		l++;
	}

////////////////////////////////////
//  remplissage inverse colone   //
//////////////////////////////////
	

	l = 1;
	while(l < 5)
	{	
		if(tab[5][l] == 4)
		{
			i = 1;
			h = 4;	
			while(h > 0)
			{
				tab[h][l] = i;
				h--;
				i++;
			}	
		}
		l++;
	}




}
