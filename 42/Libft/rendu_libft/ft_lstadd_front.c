#include "libft.h"

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	new->next = *alst;
	*alst = new;
}

/*		*alst = new : car *alst represente toujours le pointeur du premier 
 *		element de la list dont new est desormais le premier element		*/
