#include <stddef.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (n != 0)
	{
		if (s1[i] != s2[i])
			break ;
		i++;
		n--;
	}
	return (s1[i] - s2[i]);
}
