#include <stddef.h>

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (const char)c)
			return ((char *)s + i);
		i++;
	}
	return (0);
}
