#include "libftptf.h"

int	is_variable(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd')
		return (0);
	if (c == 'i' || c == 'u' || c == 'x' || c == 'X')
		return (0);
	return (1);
}
